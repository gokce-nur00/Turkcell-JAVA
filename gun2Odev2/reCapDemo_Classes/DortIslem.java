package gun2odev2.reCapDemo_Classes;

public class DortIslem {
    public int Topla(int a, int b) {
        return a + b;
    }

    public int Cikarma(int a, int b) {
        return a - b;
    }

    public int Carpma(int a, int b) {
        return a * b;
    }

    public int Bolme(int a, int b) {
        return a / b;
    }
}
